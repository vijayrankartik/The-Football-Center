document.getElementById("gkc").addEventListener("click", function(){
    for(let c = 0;c<20;c++)
    {
        gkfin(c);
    }
});
function gkfin(c)
{
    setTimeout(function()
    {
        var i = Math.floor(Math.random()*5)+1
        // console.log(i)
        if(i == 1)
        document.getElementById("gkc").src = "./renders/gk1.png"
        if(i == 2)
        document.getElementById("gkc").src = "./renders/gk2.png"
        if(i == 3)
        document.getElementById("gkc").src = "./renders/gk3.png"
        if(i == 4)
        document.getElementById("gkc").src = "./renders/gk4.png"
        if(i == 5)
        document.getElementById("gkc").src = "./renders/gk5.png"
    },200*c)
}


